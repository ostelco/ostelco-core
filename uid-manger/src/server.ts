import * as DebugAgent from "@google-cloud/debug-agent";
import app from "./app";

DebugAgent.start();
const server = app.listen(app.get("port"), () => {
  console.log(
    "  App is running at http://localhost:%d in %s mode",
    app.get("port"),
    app.get("env")
  );
  console.log("  Press CTRL-C to stop\n");
});

export default server;
